    private void datatable(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID Barang");
        tbl.addColumn("Nama Barang");
        tbl.addColumn("Harga Barang");
        tblTampilDataBarang.setModel(tbl);
         try{
             
             Statement stat3 = (Statement) koneksi01.getKoneksi().createStatement( );
             ResultSet res3 = stat3.executeQuery("select * from barang");
             while(res3.next())
             {
                 tbl.addRow(new Object[] {
                     res3.getString("id_barang"),
                     res3.getString("nama_barang"),
                     res3.getString("Harga_barang"),
                 });
                 tblTampilDataBarang.setModel(tbl);
             }
         }catch (Exception e){
             JOptionPane.showMessageDialog(rootPane, "salah");
         }
    }
    
    
    
