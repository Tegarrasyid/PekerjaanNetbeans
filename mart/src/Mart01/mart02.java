    private void txtJmlHargaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJmlHargaKeyPressed
        // TODO add your handling code here:
        
        List baris = new ArrayList<>();
        tblBarang.setAutoCreateColumnsFromModel(true);
        baris.add(cmbBarang.getSelectedItem().toString());
        baris.add(txtHarga.getText());
        baris.add(txtJmlHarga.getText());
        
        
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            if(barisTerpilih){
                btnEditClick();
            }else{
              subTotal();
            baris.add(String.valueOf(subTotalBelanja));
            dataModel.addRow(baris.toArray());
            
            total();
            clear();  
            }  
        }
        if (evt.getKeyCode() == KeyEvent.VK_DELETE){
            if (barisTerpilih){
                KeyDelete();
            }
            tblBarang.clearSelection();
            barisTerpilih = false;
        }
    }//GEN-LAST:event_txtJmlHargaKeyPressed

