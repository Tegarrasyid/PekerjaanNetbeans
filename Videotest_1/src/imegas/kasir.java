    private void tblBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangMouseClicked
        // TODO add your handling code here:
        baris = tblBarang.getSelectedRow();
        if(baris >= 0){
        barisTerpilih = true;
        String namaBarang = (String) tblBarang.getValueAt(baris, 0);
        String hargaSatuan = (String) tblBarang.getValueAt(baris, 1);
        String jumlahBeli = (String) tblBarang.getValueAt(baris, 2);
        
        cmbBarang.setSelectedItem(namaBarang);
        lblHarga.setText(hargaSatuan);
        txtHarga.setText(jumlahBeli);
        }else {
            barisTerpilih = false;
        }
    }//GEN-LAST:event_tblBarangMouseClicked

