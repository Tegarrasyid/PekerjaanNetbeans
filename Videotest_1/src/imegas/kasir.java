    private int subTotal (){
        int jumlahBeli, hargaSatuan;
        jumlahBeli = Integer.valueOf(txtHarga.getText());
        hargaSatuan = Integer.valueOf(lblHarga.getText());
        subTotalBelanja = jumlahBeli*hargaSatuan;
        return subTotalBelanja;
        
    }

    
    private void total() {
        int jumlah = dataModel.getRowCount();
        int sumTotal = 0;
        for (int i = 0 ; i < jumlah; i++){
            int dataTotal = Integer.valueOf(dataModel.getValueAt(i,3).toString());
            sumTotal += dataTotal;
        }
        lblTotalHarga.setText(Integer.toString(sumTotal));
    }
    
    private void clear(){
        txtHarga.setText("");
        lblHarga.setText("");
        cmbBarang.setSelectedIndex(0);
    }

    private void btnEditClick() {
        //cekbaristerpilih
        String nmBarang = (String) cmbBarang.getSelectedItem();
        String hrgSatuan = (String) lblHarga.getText ();
        String jmlBeli = (String) txtHarga.getText();
        
        subTotal();
        dataModel.setValueAt(nmBarang, baris, 0);
        dataModel.setValueAt(hrgSatuan, baris, 1);
        dataModel.setValueAt(jmlBeli, baris, 2);
        dataModel.setValueAt(subTotalBelanja, baris, 3);
        
        clear();
        total();
        tblBarang.clearSelection();
        barisTerpilih=false;
    }
    
    private int jumlahBarisTable(){
        jumlahBaris =tblBarang.getRowCount();
        return jumlahBaris;
    }
    
    private boolean cekBarisTerpilih(){
        return true;
    }

    private void KeyDelete() {
        baris = tblBarang.getSelectedRow();
        barisTerpilih=true;
        dataModel.removeRow(baris);
        
        total();
        clear();
        barisTerpilih=false;
    }
