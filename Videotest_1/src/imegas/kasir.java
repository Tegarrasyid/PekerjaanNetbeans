    private void txtHargaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtHargaKeyPressed
        // TODO add your handling code here:
        List baris = new ArrayList<>();
        tblBarang.setAutoCreateColumnsFromModel(true);
        baris.add(cmbBarang.getSelectedItem().toString());
        baris.add(lblHarga.getText());
        baris.add(txtHarga.getText());
        
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            if(barisTerpilih){
                btnEditClick();
            }else{
              subTotal();
            baris.add(String.valueOf(subTotalBelanja));
            dataModel.addRow(baris.toArray());
            
            total();
            clear();  
            }  
        }
        if (evt.getKeyCode() == KeyEvent.VK_DELETE){
            if (barisTerpilih){
                KeyDelete();
            }
            tblBarang.clearSelection();
            barisTerpilih = false;
        }
    }//GEN-LAST:event_txtHargaKeyPressed

